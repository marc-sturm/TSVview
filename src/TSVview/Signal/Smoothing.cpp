void Smoothing::smooth(QVector<double>& data, Type type, Parameters params)
{
	switch(type)
	{
		case MovingAverage:
			movingAverage_(data, params);
			break;
		case MovingMedian:
			movingMedian_(data, params);
			break;
		case SavitzkyGolay:
			savitzkyGolay_(data, params);
			break;
	}
}

Parameters Smoothing::defaultParameters(Type type)
{
	Parameters params;

	switch(type)
	{
		case MovingAverage:
			params.addInt("window size", "", 5, 3);
			break;
		case MovingMedian:
			params.addInt("window size", "", 5, 3);
			break;
		case SavitzkyGolay:
			params.addInt("window size", "", 5, 5, 101);
			break;
	}

	return params;
}

void Smoothing::movingAverage_(QVector<double>& data, Parameters params)
{
	//IMPROVEMENT: do it with one array
	QVector<double> output = data;

	int window = params.getInt("window size");
	if (window%2==0)
	{
		++window;
	}

	int hws = window/2;
	for (int i=hws; i<data.count()-hws; ++i)
	{
		double sum = 0.0;
		for (int j=i-hws; j<=i+hws; ++j)
		{
			sum += data.at(j);
		}
		output[i] = sum / window;
	}

	data = output;
}

void Smoothing::movingMedian_(QVector<double>& data, Parameters params)
{
	//IMPROVEMENT: do it with one array
	QVector<double> output = data;

	int window = params.getInt("window size");
	if (window%2==0)
	{
		++window;
	}

	int hws = window/2;
	for (int i=hws; i<data.count()-hws; ++i)
	{
		//IMPROVEMENT: keep the array and remove/add the border values
		QVector<double> values;
		for (int j=i-hws; j<=i+hws; ++j)
		{
			values.append(data.at(j));
		}
		qSort(values);
		output[i] = values[hws];
	}

	data = output;
}

void Smoothing::savitzkyGolay_(QVector<double>& data, Parameters params)
{
	//IMPROVEMENT: do it with one array
	QVector<double> output(data);

	int window_size = params.getInt("window size");
	if (window_size%2==0)
	{
		++window_size;
	}
	int hws = window_size/2;

	//precalculated coefficients
	QVarLengthArray<double> coeffs = getSGCoeffs_(window_size);

	//calculate SG-smoothing
	for (int i=hws; i<data.size()-1-hws; ++i)
	{
		double sum = 0.0;
		for (int j=-hws ;j<=hws ;++j)
		{
			sum += data[i+j] * coeffs[hws+j];
		}
		output[i] = sum;
	}

	data = output;
}

